def process_one(input_path, combined):
    print(f"\n▶ {os.path.basename(input_path)}")
    wb = load_workbook(input_path)

    # per-sheet transformations
    s = process_lldp(wb, combined);              print(f"  • LLDP            -> {s}")
    s = process_lldp_missing(wb, combined);      print(f"  • LLDP Missing    -> {s}")
    s = process_optic(wb, combined);             print(f"  • Optic Errors    -> {s}")
    s = process_fec(wb, combined);               print(f"  • FEC_BER         -> {s}")
    s = process_rawber(wb, combined);            print(f"  • RAW_BER         -> {s}")
    s = process_interface_down(wb, combined);    print(f"  • Interface Down  -> {s}")

    # Summary refresh
    update_summary(wb)

    # workbook-wide formatting (must run AFTER all column ops)
    apply_workbook_formatting(wb)

    # group reciprocal mismatch pairs + highlight (after formatting)
    highlight_mismatch_pairs(wb)

    # output filename from rack numbers
    racks = extract_rack_number(wb)
    fname = ("-".join(racks) if racks else os.path.splitext(os.path.basename(input_path))[0]) + ".xlsx"
    out_dir = os.path.dirname(os.path.abspath(input_path))
    out_path = os.path.join(out_dir, fname)
    # if collision, append (1), (2), ...
    base, ext = os.path.splitext(out_path)
    n = 1
    while os.path.exists(out_path) and os.path.abspath(out_path) != os.path.abspath(input_path):
        out_path = f"{base} ({n}){ext}"
        n += 1

    wb.save(out_path)
    print(f"  ✓ saved: {out_path}")
    return out_path


