def norm(v):
    return str(v).strip().lower() if v is not None else ""


def canon_slot(p):
    """slot.../port...-1 and -2 -> ...-1   ;   -3 and -4 -> ...-3"""
    if not p: return ""
    s = norm(p)
    m = re.match(r'^(.*-)(\d+)$', s)
    if m:
        base, num = m.group(1), int(m.group(2))
        if num in (1, 2): return base + "1"
        if num in (3, 4): return base + "3"
    return s


def canon_swp(p):
    """swpNs0/s1 -> swpNs0   ;   swpNs2/s3 -> swpNs2"""
    if not p: return ""
    s = norm(p)
    m = re.match(r'^(swp\d+s)(\d+)$', s)
    if m:
        base, num = m.group(1), int(m.group(2))
        return base + str(num - num % 2)
    return s


def snapshot_col(ws, col_idx):
    """Capture a column's values + cell formatting + width so it can be moved."""
    data = []
    for r in range(1, ws.max_row + 1):
        src = ws.cell(row=r, column=col_idx)
        data.append({
            'value': src.value,
            'font': copy(src.font),
            'fill': copy(src.fill),
            'alignment': copy(src.alignment),
            'border': copy(src.border),
            'number_format': src.number_format,
        })
    width = ws.column_dimensions[get_column_letter(col_idx)].width
    return data, width


def write_col(ws, col_idx, data, width):
    for r, d in enumerate(data, start=1):
        c = ws.cell(row=r, column=col_idx, value=d['value'])
        c.font = d['font']; c.fill = d['fill']
        c.alignment = d['alignment']; c.border = d['border']
        c.number_format = d['number_format']
    if width:
        ws.column_dimensions[get_column_letter(col_idx)].width = width


def find_sheet(wb, *candidates):
    """Return the first matching sheet name (case-insensitive, ignoring extra spaces)."""
    lookup = {re.sub(r'\s+', ' ', s.strip().lower()): s for s in wb.sheetnames}
    for c in candidates:
        key = re.sub(r'\s+', ' ', c.strip().lower())
        if key in lookup:
            return lookup[key]
    return None


