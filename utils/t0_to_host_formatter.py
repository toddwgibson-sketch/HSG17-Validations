def process_rawber(wb, combined):
    combined_header, exact_A, pair_A, exact_B, pair_B = combined
    gpu  = find_sheet(wb, "RAW_BER Errors (GPU)", "RAW_BER Errors", "Fec Errors")
    host = find_sheet(wb, "RAW_BER Errors (Host)")
    if not gpu and not host:
        return None

    grey_rows = []

    if gpu:
        sheet = gpu
        ws = wb[sheet]
        # 0) combine the Host RAW_BER tab into this one, then delete it. As with
        #    the optics, the compute device (the cutsheet-match side) lives in
        #    Source Device Name/Port on the host tab, so map those into the GPU
        #    tab's Device Name / Interface columns before matching.
        if host and host != sheet:
            hws = wb[host]
            gpu_hdr = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
            host_hdr = [hws.cell(row=1, column=c).value for c in range(1, hws.max_column+1)]
            col_of = {h: i + 1 for i, h in enumerate(gpu_hdr)}
            hidx = {h: i + 1 for i, h in enumerate(host_hdr)}

            def hval(r, name):
                return hws.cell(row=r, column=hidx[name]).value if name in hidx else None

            dest = ws.max_row + 1
            for r in range(2, hws.max_row + 1):
                if all(hws.cell(row=r, column=c).value in (None, "")
                       for c in range(1, hws.max_column + 1)):
                    continue
                remap = {
                    'Device Name': hval(r, 'Source Device Name'),
                    'Interface':   hval(r, 'Source Device Port'),
                }
                for h, val in remap.items():
                    if h in col_of:
                        ws.cell(row=dest, column=col_of[h], value=val)
                # flag rows whose original Raw BER / Status read "Not Available" / "Missing"
                if norm(hval(r, 'Raw BER')) == 'not available' and norm(hval(r, 'Status')) == 'missing':
                    grey_rows.append(dest)
                dest += 1
            del wb[host]
    else:
        # Host-only input: rebuild the Host tab into a [Device Name, Interface]
        # base (Source Name/Port = the compute side), then match the same way.
        sheet = host
        ws = wb[sheet]
        hdr = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
        hidx = {h: i + 1 for i, h in enumerate(hdr)}

        def hv(r, name):
            return ws.cell(row=r, column=hidx[name]).value if name in hidx else None

        extracted = []
        for r in range(2, ws.max_row + 1):
            if all(ws.cell(row=r, column=c).value in (None, "")
                   for c in range(1, ws.max_column + 1)):
                continue
            grey = (norm(hv(r, 'Raw BER')) == 'not available'
                    and norm(hv(r, 'Status')) == 'missing')
            extracted.append((hv(r, 'Source Device Name'), hv(r, 'Source Device Port'), grey))

        ws.delete_cols(1, ws.max_column)
        ws.cell(row=1, column=1, value='Device Name')
        ws.cell(row=1, column=2, value='Interface')
        for i, (dn, it, grey) in enumerate(extracted, start=2):
            ws.cell(row=i, column=1, value=dn)
            ws.cell(row=i, column=2, value=it)
            if grey:
                grey_rows.append(i)

    # 1) match & populate via Device Name + Interface
    populate(ws, 'Device Name', 'Interface',
             exact_A, pair_A, canon_slot, combined_header)

    # 2) remove unwanted columns
    drop_cols_by_name(ws, ['Lane Values', 'Issue', 'Patch Panel Matrix', 'Error Message'])

    # 2b) light-grey the font on host rows that were "Not Available" / "Missing",
    #     and note "not available" on those rows.
    grey_color = 'A6A6A6'
    if grey_rows:
        header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
        if 'Note' not in header:
            add_note_column(ws)
            header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
        note_idx = header.index('Note') + 1
        for r in grey_rows:
            for c in range(1, ws.max_column + 1):
                cell = ws.cell(row=r, column=c)
                f = cell.font
                cell.font = Font(
                    name=f.name, size=f.size, bold=f.bold, italic=f.italic,
                    vertAlign=f.vertAlign, underline=f.underline, strike=f.strike,
                    color=grey_color, family=f.family, scheme=f.scheme
                )
            ws.cell(row=r, column=note_idx, value='not available')

    # 2c) cross-reference Optic Errors: if the same link (Device Name +
    #     Interface here == Remote Device Name + Remote Device Port there)
    #     also appears in the Optic Errors tab, light-grey the row font and
    #     note "also optic error".
    optic = find_sheet(wb, "Optic Errors", "Optic Errors (GPU)")
    if optic:
        ows = wb[optic]
        oh = [ows.cell(row=1, column=c).value for c in range(1, ows.max_column+1)]
        if 'Remote Device Name' in oh and 'Remote Device Port' in oh:
            oni = oh.index('Remote Device Name') + 1
            opi = oh.index('Remote Device Port') + 1
            optic_links = set()
            for r in range(2, ows.max_row + 1):
                nm = ows.cell(row=r, column=oni).value
                if nm:
                    optic_links.add((norm(nm), norm(ows.cell(row=r, column=opi).value)))

            header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
            if 'Device Name' in header and 'Interface' in header:
                if 'Note' not in header:
                    add_note_column(ws)
                    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
                dn_idx = header.index('Device Name') + 1
                it_idx = header.index('Interface') + 1
                note_idx = header.index('Note') + 1
                for r in range(2, ws.max_row + 1):
                    key = (norm(ws.cell(row=r, column=dn_idx).value),
                           norm(ws.cell(row=r, column=it_idx).value))
                    if key in optic_links:
                        for c in range(1, ws.max_column + 1):
                            cell = ws.cell(row=r, column=c)
                            f = cell.font
                            cell.font = Font(
                                name=f.name, size=f.size, bold=f.bold, italic=f.italic,
                                vertAlign=f.vertAlign, underline=f.underline, strike=f.strike,
                                color=grey_color, family=f.family, scheme=f.scheme
                            )
                        ws.cell(row=r, column=note_idx, value='also optic error')

    # 3) rename combined tab
    if ws.title != "Fec Errors":
        ws.title = "Fec Errors"

    return ws.title


def process_lldp_missing(wb, combined):
    combined_header, exact_A, pair_A, exact_B, pair_B = combined
    sheet = find_sheet(wb, "LLDP Missing (GPU)", "LLDP Missing  (GPU)")
    if not sheet:
        return None
    ws = wb[sheet]

    # 1) match & populate via Expected Device B Name + Expected Device B Port
    populate(ws, 'Expected Device B Name', 'Expected Device B Port',
             exact_A, pair_A, canon_slot, combined_header)

    # 2) keep Error Message + Expected Device B Name/Port (+ the appended cutsheet
    #    columns); drop the rest. The Note column is added later by
    #    apply_workbook_formatting, so it is preserved automatically.
    drop_cols_by_name(ws, [
        'Device A Name', 'Device A Location', 'Device A Port',
        'Current Device B Name', 'Current B Location', 'Current Device B Port',
        'Expected B Location',
        'Patch Panel Matrix',
    ])

    # 3) order: Error Message, Expected Device B Name, Expected Device B Port,
    #    then the populated cutsheet columns
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    lead = [h for h in ('Error Message', 'Expected Device B Name',
                        'Expected Device B Port') if h in header]
    rest = [h for h in header if h not in lead]
    reorder_columns(ws, lead + rest)

    # 4) trim Error Message: keep only from "Observed remote" onward
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    if 'Error Message' in header:
        em_idx = header.index('Error Message') + 1
        for r in range(2, ws.max_row + 1):
            v = ws.cell(row=r, column=em_idx).value
            if v is None:
                continue
            s = str(v)
            pos = s.find('Observed remote')
            if pos != -1:
                ws.cell(row=r, column=em_idx, value=s[pos:])

    # 5) rename tab
    if ws.title != "LLDP missing (IGNORE)":
        ws.title = "LLDP missing (IGNORE)"

    return ws.title


