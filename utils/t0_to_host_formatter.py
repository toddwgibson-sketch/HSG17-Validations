def _find_header_idx(header, name_options):
    """Return the 0-based index of the first header matching any of name_options
    (case-insensitive, whitespace-tolerant), or None if not found."""
    for opt in name_options:
        key = re.sub(r'\s+', ' ', str(opt).strip().lower())
        for i, h in enumerate(header):
            if h is None:
                continue
            if re.sub(r'\s+', ' ', str(h).strip().lower()) == key:
                return i
    return None


def build_combined_lookups(combined_path):
    """
    Reads the combined cutsheet's first sheet (any name) and builds 4 lookups:
        exact_A, pair_A keyed on (DeviceA, PortA)
        exact_B, pair_B keyed on (DeviceB, PortB)
    Columns are located BY HEADER NAME so the script tolerates layout
    variations (e.g. a missing FDF column).
    Returns (header_list, exact_A, pair_A, exact_B, pair_B).
    """
    wb = load_workbook(combined_path, read_only=True, data_only=True)
    ws = wb[wb.sheetnames[0]]

    header = None
    rows = []
    for i, row in enumerate(ws.iter_rows(values_only=True)):
        if i == 0:
            header = list(row)
        else:
            rows.append(row)

    # ---- Detect ALLCONNECTIONS format (e.g. QFABT0toHOST_allconnections) ----
    # Keys live in 'DeviceA Name'/'DeviceA Port' (compute side) and
    # 'DeviceB Name'/'DeviceB Port' (T0 switch side). The plain 'DeviceA'
    # column there is a combined "device + port" string, NOT the key.
    ac_a_dev  = _find_header_idx(header, ['DeviceA Name'])
    ac_a_port = _find_header_idx(header, ['DeviceA Port'])

    if ac_a_dev is not None and ac_a_port is not None:
        ac_b_dev  = _find_header_idx(header, ['DeviceB Name'])
        ac_b_port = _find_header_idx(header, ['DeviceB Port'])

        # Cable-path + label columns to carry into the report (in this order).
        # 'DeviceA'/'DeviceB' are renamed so they don't collide with the
        # populate() skip list (which drops 'DeviceA' for the legacy format).
        keep = [
            ('DeviceA',                 'DeviceA (Cutsheet)'),
            ('RackA',                   'RackA'),
            ('OHR',                     'OHR'),
            ('T0',                      'T0'),
            ('DeviceB',                 'DeviceB (Cutsheet)'),
            ('RackB',                   'RackB'),
            ('Source_port',             'Source_port'),
            ('Destination_port',        'Destination_port'),
            ('DeviceA Physical Port',   'DeviceA Physical Port'),
            ('DeviceB Physical Port',   'DeviceB Physical Port'),
            ('Cable Color',             'Cable Color'),
        ]
        keep_idx, out_header = [], []
        for src_name, out_name in keep:
            i = _find_header_idx(header, [src_name])
            if i is not None:
                keep_idx.append(i)
                out_header.append(out_name)

        exact_A, pair_A, exact_B, pair_B = {}, {}, {}, {}
        for r in rows:
            reduced = tuple(r[i] if i < len(r) else None for i in keep_idx)
            if ac_a_dev < len(r) and r[ac_a_dev]:
                port_val = r[ac_a_port] if ac_a_port < len(r) else None
                exact_A.setdefault((norm(r[ac_a_dev]), norm(port_val)), reduced)
                pair_A.setdefault((norm(r[ac_a_dev]), canon_slot(port_val)), reduced)
            if (ac_b_dev is not None and ac_b_port is not None
                    and ac_b_dev < len(r) and r[ac_b_dev]):
                port_val = r[ac_b_port] if ac_b_port < len(r) else None
                exact_B.setdefault((norm(r[ac_b_dev]), norm(port_val)), reduced)
                pair_B.setdefault((norm(r[ac_b_dev]), canon_swp(port_val)), reduced)
        return out_header, exact_A, pair_A, exact_B, pair_B

    # ---- Legacy COMBINED format (DeviceA/PortA/DeviceB/PortB) ----
    # Locate join-key columns by header name (not by position).
    a_dev_i  = _find_header_idx(header, ['DeviceA', 'Device A'])
    a_port_i = _find_header_idx(header, ['PortA', 'Port A'])
    b_dev_i  = _find_header_idx(header, ['DeviceB', 'Device B'])
    b_port_i = _find_header_idx(header, ['PortB', 'Port B'])

    if a_dev_i is None or a_port_i is None:
        raise ValueError(
            "Combined cutsheet is missing required A-side columns "
            "('DeviceA'+'PortA' or 'DeviceA Name'+'DeviceA Port'). "
            "Found headers: " + repr(header)
        )

    exact_A, pair_A, exact_B, pair_B = {}, {}, {}, {}
    for r in rows:
        if a_dev_i < len(r) and r[a_dev_i]:
            port_val = r[a_port_i] if a_port_i < len(r) else None
            exact_A.setdefault((norm(r[a_dev_i]), norm(port_val)), r)
            pair_A.setdefault((norm(r[a_dev_i]), canon_slot(port_val)), r)
        if (b_dev_i is not None and b_port_i is not None
                and b_dev_i < len(r) and r[b_dev_i]):
            port_val = r[b_port_i] if b_port_i < len(r) else None
            exact_B.setdefault((norm(r[b_dev_i]), norm(port_val)), r)
            pair_B.setdefault((norm(r[b_dev_i]), canon_swp(port_val)), r)
    return header, exact_A, pair_A, exact_B, pair_B


