def update_summary(wb):
    sheet = find_sheet(wb, "Summary")
    if not sheet:
        return
    ws = wb[sheet]
    existing_sheets = set(wb.sheetnames)

    name_map = {
        'lldp mismatch + link down (gpu)': find_sheet(wb, "Mismatch", "LLDP Mismatch  (GPU)", "LLDP Mismatch (GPU)"),
        'lldp mismatch  (gpu)':            find_sheet(wb, "Mismatch", "LLDP Mismatch  (GPU)", "LLDP Mismatch (GPU)"),
        'lldp mismatch (gpu)':             find_sheet(wb, "Mismatch", "LLDP Mismatch  (GPU)", "LLDP Mismatch (GPU)"),
        'optic errors (gpu)':              find_sheet(wb, "Optic Errors", "Optic Errors (GPU)"),
        'optic errors (host)':             find_sheet(wb, "Optic Errors", "Optic Errors (GPU)"),
        'lldp missing (gpu)':              None,
        'raw_ber errors (gpu)':            find_sheet(wb, "Fec Errors", "RAW_BER Errors", "RAW_BER Errors (GPU)"),
        'raw_ber errors (host)':           find_sheet(wb, "Fec Errors", "RAW_BER Errors", "RAW_BER Errors (GPU)"),
        'fec_ber errors (gpu)':            find_sheet(wb, "Fec Errors", "FEC_BER Errors (GPU)"),
        'interface down errors (gpu)':     find_sheet(wb, "Interface Down Errors", "Interface Down Errors (GPU)"),
    }

    # rebuild rows: keep only categories that map to a real sheet.
    # Count is the number of data rows whose font is NOT light grey (A6A6A6),
    # i.e. greyed rows (CT-off, -40 optics, "also interface down", host
    # "Not Available/Missing") are excluded from the count.
    def _is_grey(cell):
        col = cell.font.color if cell.font else None
        rgb = getattr(col, 'rgb', None)
        return isinstance(rgb, str) and rgb.upper().endswith('A6A6A6')

    def count_non_grey(tsheet):
        tws = wb[tsheet]
        n = 0
        for r in range(2, tws.max_row + 1):
            a = tws.cell(row=r, column=1)
            if a.value not in (None, "") and not _is_grey(a):
                n += 1
        return n

    rows_keep = [(ws.cell(row=1, column=1).value, ws.cell(row=1, column=2).value)]
    seen_targets = set()
    for r in range(2, ws.max_row + 1):
        cat = ws.cell(row=r, column=1).value
        target = name_map.get(norm(cat))
        if target and target in existing_sheets and target not in seen_targets:
            seen_targets.add(target)
            rows_keep.append((target, count_non_grey(target)))

    # append Total row that sums the category counts
    if len(rows_keep) > 1:
        first_data_row = 2
        last_data_row = len(rows_keep)
        rows_keep.append(("Total", f"=SUM(B{first_data_row}:B{last_data_row})"))

    # clear & rewrite
    for r in range(1, ws.max_row + 1):
        for c in range(1, ws.max_column + 1):
            ws.cell(row=r, column=c).value = None
    for i, (a, b) in enumerate(rows_keep, start=1):
        ws.cell(row=i, column=1, value=a)
        ws.cell(row=i, column=2, value=b)

    # bold the Total row so it stands out
    if len(rows_keep) > 1:
        total_row = len(rows_keep)
        for c in (1, 2):
            cell = ws.cell(row=total_row, column=c)
            f = cell.font
            cell.font = Font(
                name=f.name, size=f.size, bold=True, italic=f.italic,
                vertAlign=f.vertAlign, underline=f.underline, strike=f.strike,
                color=f.color, family=f.family, scheme=f.scheme
            )

    # delete trailing empty rows beyond rows_keep
    for r in range(ws.max_row, len(rows_keep), -1):
        ws.delete_rows(r, 1)

def apply_workbook_formatting(wb):
    yellow = PatternFill(start_color='FFFF00', end_color='FFFF00', fill_type='solid')
    center = Alignment(horizontal='center', vertical='center', wrap_text=False)
    thin   = Side(border_style='thin', color='000000')
    border = Border(left=thin, right=thin, top=thin, bottom=thin)

    for sn in wb.sheetnames:
        ws = wb[sn]
        if ws.max_row == 0 or ws.max_column == 0:
            continue

        # add Note column (skip Summary)
        if sn.lower() != "summary":
            # only add if not already present
            existing = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
            if 'Note' not in existing:
                add_note_column(ws)

        # alignment + borders on all used cells
        for r in range(1, ws.max_row + 1):
            for c in range(1, ws.max_column + 1):
                cell = ws.cell(row=r, column=c)
                cell.alignment = center
                cell.border = border

        # yellow + bold + size+1 on header
        for c in range(1, ws.max_column + 1):
            cell = ws.cell(row=1, column=c)
            cell.fill = yellow
            f = cell.font
            size = f.size if f.size else 11
            cell.font = Font(
                name=f.name, size=size + 1, bold=True, italic=f.italic,
                vertAlign=f.vertAlign, underline=f.underline, strike=f.strike,
                color=f.color, family=f.family, scheme=f.scheme
            )

        # auto-filter
        last_col = get_column_letter(ws.max_column)
        ws.auto_filter.ref = f"A1:{last_col}{ws.max_row}"

        # uniform row height
        for r in range(1, ws.max_row + 1):
            ws.row_dimensions[r].height = 22

        # column widths sized to content (cap 60)
        for c in range(1, ws.max_column + 1):
            col_letter = get_column_letter(c)
            max_len = max((len(str(ws.cell(row=r, column=c).value or "")) for r in range(1, ws.max_row+1)), default=10)
            ws.column_dimensions[col_letter].width = min(max(max_len + 2, 10), 60)

        # default freeze: header row
        ws.freeze_panes = 'A2'

    # per-sheet freeze overrides
    s = find_sheet(wb, "Optic Errors", "Optic Errors (GPU)")
    if s: wb[s].freeze_panes = 'B2'   # freeze header + Rx Power (col A)
    s = find_sheet(wb, "FEC_BER Errors (GPU)")
    if s: wb[s].freeze_panes = 'B2'   # freeze header + Issue (col A)
    s = find_sheet(wb, "LLDP missing (IGNORE)", "LLDP Missing (GPU)", "LLDP Missing  (GPU)")
    if s: wb[s].freeze_panes = 'B2'   # freeze header + Error Message (col A)


