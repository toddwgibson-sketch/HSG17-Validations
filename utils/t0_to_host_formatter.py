def highlight_mismatch_pairs(wb):
    """On the LLDP Mismatch (GPU) tab, group reciprocal swap pairs + highlight.

    Two rows are a *pair* when one row's actual neighbour (Current Device B
    Name + Port) is exactly the other row's intended neighbour (Expected
    Device B Name + Port) AND vice-versa — i.e. the two links are crossed
    (each took the other's destination).

    Behaviour mirrors the FAB/CFAB formatters:
      • paired rows are grouped together (partner inserted directly beneath),
      • each pair's white (Expected-side) block is filled, alternating 1st
        pair orange, 2nd pair yellow, 3rd orange, …  (Current cols stay pink),
      • rows with no pair are pushed to the bottom with no highlight.
    """
    sheet = find_sheet(wb, "Mismatch", "LLDP Mismatch  (GPU)", "LLDP Mismatch (GPU)",
                       "LLDP Mismatch + Link Down (GPU)")
    if not sheet:
        return
    ws = wb[sheet]
    if ws.max_row < 3:
        return

    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column + 1)]

    def col(name):
        return header.index(name) + 1 if name in header else None

    exp_name = col('Expected Device B Name')
    exp_port = col('Expected Device B Port')
    cur_name = col('Current Device B Name')
    cur_port = col('Current Device B Port')
    if not all((exp_name, exp_port, cur_name, cur_port)):
        return

    pink_cols  = [i + 1 for i, h in enumerate(header) if 'current' in str(h).lower()]
    start_pink = min(pink_cols) if pink_cols else ws.max_column + 1
    white_cols = set(range(1, start_pink))     # colour these on paired rows
    pink_set   = set(pink_cols)                # stay pink

    ncol, nrow = ws.max_column, ws.max_row
    rows = [[ws.cell(row=r, column=c).value for c in range(1, ncol + 1)]
            for r in range(2, nrow + 1)]

    exp_keys = [(norm(rv[exp_name - 1]), norm(rv[exp_port - 1])) for rv in rows]
    cur_keys = [(norm(rv[cur_name - 1]), norm(rv[cur_port - 1])) for rv in rows]

    n = len(rows)
    partner = [None] * n
    for i in range(n):
        if partner[i] is not None or not any(exp_keys[i]):
            continue
        for j in range(i + 1, n):
            if partner[j] is not None:
                continue
            if cur_keys[i] == exp_keys[j] and cur_keys[j] == exp_keys[i]:
                partner[i] = j
                partner[j] = i
                break

    # Pairs first (partner directly beneath its match), unpaired at the end.
    placed = [False] * n
    order = []
    for i in range(n):
        if placed[i] or partner[i] is None:
            continue
        j = partner[i]
        order.append(i); placed[i] = True
        if not placed[j]:
            order.append(j); placed[j] = True
    for i in range(n):
        if not placed[i]:
            order.append(i); placed[i] = True

    paired = {i for i in range(n) if partner[i] is not None}

    # Number pairs in order of first appearance for alternating colours.
    pair_no, counter = {}, 0
    for i in range(n):
        j = partner[i]
        if j is not None and i < j:
            pair_no[i] = counter
            pair_no[j] = counter
            counter += 1

    orange = PatternFill(start_color='FFA500', end_color='FFA500', fill_type='solid')
    yellow = PatternFill(start_color='FFFF00', end_color='FFFF00', fill_type='solid')
    pink   = PatternFill(start_color='FFC0CB', end_color='FFC0CB', fill_type='solid')
    nofill = PatternFill(fill_type=None)

    for out_off, src_i in enumerate(order):
        r = out_off + 2
        rv = rows[src_i]
        is_pair = src_i in paired
        pair_fill = orange if pair_no.get(src_i, 0) % 2 == 0 else yellow
        for c in range(1, ncol + 1):
            cell = ws.cell(row=r, column=c, value=rv[c - 1])
            if c in pink_set:
                cell.fill = pink
            elif is_pair and c in white_cols:
                cell.fill = pair_fill
            else:
                cell.fill = nofill


