def mark_ct_off(ws, key_col_name='Remote Device Name',
                threshold=16, note_text='maybe CT off',
                grey_color='A6A6A6'):
    """
    Count occurrences of `key_col_name` across data rows. For each row whose
    value appears exactly `threshold` times, set whole-row font to grey and
    write `note_text` into the Note column (created at the back if missing).
    """
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    if key_col_name not in header:
        return 0
    key_idx = header.index(key_col_name) + 1

    if 'Note' not in header:
        # add_note_column is defined later in this module; safe at call time
        new_col = ws.max_column + 1
        src = ws.cell(row=1, column=1)
        cell = ws.cell(row=1, column=new_col, value='Note')
        cell.font = copy(src.font); cell.fill = copy(src.fill)
        cell.alignment = copy(src.alignment); cell.border = copy(src.border)
        ws.column_dimensions[get_column_letter(new_col)].width = 30
        header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    note_idx = header.index('Note') + 1

    from collections import Counter
    counts = Counter()
    for r in range(2, ws.max_row + 1):
        v = ws.cell(row=r, column=key_idx).value
        if v: counts[str(v).strip()] += 1
    targets = {k for k, c in counts.items() if c == threshold}

    marked = 0
    for r in range(2, ws.max_row + 1):
        v = ws.cell(row=r, column=key_idx).value
        if v and str(v).strip() in targets:
            for c in range(1, ws.max_column + 1):
                cell = ws.cell(row=r, column=c)
                f = cell.font
                cell.font = Font(
                    name=f.name, size=f.size, bold=f.bold, italic=f.italic,
                    vertAlign=f.vertAlign, underline=f.underline, strike=f.strike,
                    color=grey_color, family=f.family, scheme=f.scheme
                )
            ws.cell(row=r, column=note_idx, value=note_text)
            marked += 1
    return marked


def process_lldp(wb, combined):
    combined_header, exact_A, pair_A, exact_B, pair_B = combined
    sheet = find_sheet(wb, "LLDP Mismatch + Link Down (GPU)", "LLDP Mismatch  (GPU)", "LLDP Mismatch (GPU)")
    if not sheet:
        return None
    ws = wb[sheet]

    # 1) drop rows where either:
    #    (a) Current D/E/F are all "missing"            (Observed remote: missing:missing)
    #    (b) Current Device B Name does NOT contain "compute"
    #        i.e. Observed remote is a serial/number (e.g. 2610QA11K9) instead
    #        of a real compute host (jbp19-...-computeN). Only true compute-to-
    #        compute mismatches are kept for review.
    cols = {h: i+1 for i, h in enumerate(ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1))}
    cn = cols.get('Current Device B Name')
    cl = cols.get('Current B Location')
    cp = cols.get('Current Device B Port')
    if cn and cl and cp:
        rows_to_drop = []
        for r in range(2, ws.max_row + 1):
            # (a) all three current fields literally "missing"
            all_missing = all(norm(ws.cell(row=r, column=c).value) == "missing"
                              for c in (cn, cl, cp))
            # (b) Current Device B Name has a value but doesn't contain "compute"
            cn_val = ws.cell(row=r, column=cn).value
            not_compute = (
                cn_val is not None
                and str(cn_val).strip() != ""
                and "compute" not in str(cn_val).lower()
            )
            # (c) Current Device B Port is a MAC address (e.g. 08:20:e7:9c:c7:8e)
            #     — LLDP reported the neighbour's port ID as a MAC instead of a
            #     named interface; the MAC varies per device, so match the format.
            cp_val = ws.cell(row=r, column=cp).value
            port_is_mac = bool(
                cp_val is not None
                and re.fullmatch(
                    r'(?:[0-9a-fA-F]{2}[:-]){5}[0-9a-fA-F]{2}',
                    str(cp_val).strip()
                )
            )
            if all_missing or not_compute or port_is_mac:
                rows_to_drop.append(r)
        for r in reversed(rows_to_drop):
            ws.delete_rows(r, 1)

    # 2) rename sheet
    new_name = "Mismatch"
    if sheet != new_name:
        ws.title = new_name

    # 3) populate Expected-side lookup
    populate(ws, 'Expected Device B Name', 'Expected Device B Port',
             exact_A, pair_A, canon_slot, combined_header)

    # 4) remove unwanted columns
    to_remove = ['Device A Name', 'Device A Location', 'Device A Port',
                 'Current B Location', 'Expected B Location',
                 'Patch Panel Matrix', 'Error Message']
    drop_cols_by_name(ws, to_remove)

    # 5) populate Current-side lookup (suffix " (Current)")
    populate(ws, 'Current Device B Name', 'Current Device B Port',
             exact_A, pair_A, canon_slot, combined_header,
             header_suffix=" (Current)")

    # 6) reverse Current columns
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    current_cols = [h for h in header if 'current' in str(h).lower()]
    reversed_current = list(reversed(current_cols))
    new_order = []
    ci = 0
    for h in header:
        if 'current' in str(h).lower():
            new_order.append(reversed_current[ci]); ci += 1
        else:
            new_order.append(h)
    reorder_columns(ws, new_order)

    # 7) move all white (non-Current) columns to the front, pink Current to the back
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    white = [h for h in header if 'current' not in str(h).lower()]
    pink  = [h for h in header if 'current' in str(h).lower()]
    reorder_columns(ws, white + pink)

    # 8) pink fill on every Current column (header + data)
    pink_fill = PatternFill(start_color='FFC0CB', end_color='FFC0CB', fill_type='solid')
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    for ci, h in enumerate(header, start=1):
        if 'current' in str(h).lower():
            for r in range(1, ws.max_row + 1):
                ws.cell(row=r, column=ci).fill = pink_fill

    return ws.title


def process_optic(wb, combined):
    combined_header, exact_A, pair_A, exact_B, pair_B = combined
    gpu  = find_sheet(wb, "Optic Errors (GPU)", "Optic Errors")
    host = find_sheet(wb, "Optic Errors (Host)")
    if not gpu and not host:
        return None

    if gpu:
        sheet = gpu
        ws = wb[sheet]
        # 0) combine the Host optic tab into this one, then delete it so both are
        #    formatted together. NOTE: in the Host tab the compute device (the
        #    side that matches the cutsheet) lives in Source Device Name/Port,
        #    whereas in the GPU tab it lives in Remote Device Name/Port. So host
        #    Source Name/Port are mapped into the combined Remote columns — this
        #    makes the single Remote-keyed lookup match host rows on their Source.
        if host and host != sheet:
            hws = wb[host]
            gpu_hdr = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
            host_hdr = [hws.cell(row=1, column=c).value for c in range(1, hws.max_column+1)]
            col_of = {h: i + 1 for i, h in enumerate(gpu_hdr)}
            hidx = {h: i + 1 for i, h in enumerate(host_hdr)}

            def hval(r, name):
                return hws.cell(row=r, column=hidx[name]).value if name in hidx else None

            dest = ws.max_row + 1
            for r in range(2, hws.max_row + 1):
                if all(hws.cell(row=r, column=c).value in (None, "")
                       for c in range(1, hws.max_column + 1)):
                    continue
                # remap: Source (host) -> Remote (combined) so the lookup matches
                remap = {
                    'Remote Device Name': hval(r, 'Source Device Name'),
                    'Remote Device Port': hval(r, 'Source Device Port'),
                    'Rx Power':           hval(r, 'Rx Power'),
                    'Patch Panel Matrix': hval(r, 'Patch Panel Matrix'),
                }
                for h, val in remap.items():
                    if h in col_of:
                        ws.cell(row=dest, column=col_of[h], value=val)
                dest += 1
            del wb[host]
    else:
        # Host-only input: use the Host tab as the base. Remap its own Source
        # Name/Port into the Remote columns in place so the Remote-keyed lookup
        # still matches on the compute device.
        sheet = host
        ws = wb[sheet]
        hdr = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
        hidx = {h: i + 1 for i, h in enumerate(hdr)}
        if 'Source Device Name' in hidx and 'Remote Device Name' in hidx:
            for r in range(2, ws.max_row + 1):
                ws.cell(row=r, column=hidx['Remote Device Name'],
                        value=ws.cell(row=r, column=hidx['Source Device Name']).value)
                if 'Source Device Port' in hidx and 'Remote Device Port' in hidx:
                    ws.cell(row=r, column=hidx['Remote Device Port'],
                            value=ws.cell(row=r, column=hidx['Source Device Port']).value)


    # 1) drop rows where Rx Power contains "missing"
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    rx_idx = header.index('Rx Power') + 1
    to_drop = []
    for r in range(2, ws.max_row + 1):
        v = ws.cell(row=r, column=rx_idx).value
        if v is not None and "missing" in str(v).lower():
            to_drop.append(r)
    for r in reversed(to_drop):
        ws.delete_rows(r, 1)

    # 2) populate via DeviceA/PortA lookup (matched on Remote Device Name + Remote Device Port)
    populate(ws, 'Remote Device Name', 'Remote Device Port',
             exact_A, pair_A, canon_slot, combined_header)

    # 3) drop unwanted columns (Source side is dropped; Remote side is kept as the match key)
    drop_cols_by_name(ws, ['Source Device Name', 'Source Device Location', 'Source Device Port'])

    # 4) remove Patch Panel Matrix and move Rx Power to front
    drop_cols_by_name(ws, ['Patch Panel Matrix', 'Error Message'])
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    if header[0] != 'Rx Power' and 'Rx Power' in header:
        rx_idx = header.index('Rx Power') + 1
        data, width = snapshot_col(ws, rx_idx)
        ws.delete_cols(rx_idx, 1)
        ws.insert_cols(1)
        write_col(ws, 1, data, width)

    # 5) light grey fill on Rx Power cells where all instant values are -40, and grey font on whole row
    grey_fill = PatternFill(start_color='E0E0E0', end_color='E0E0E0', fill_type='solid')
    grey_color = 'A6A6A6'
    header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
    rx_idx = header.index('Rx Power') + 1
    for r in range(2, ws.max_row + 1):
        v = ws.cell(row=r, column=rx_idx).value
        s = str(v) if v is not None else ""
        nums = re.findall(r'instant:\s*(-?\d+(?:\.\d+)?)', s)
        if not nums:
            # Host rows store a bare reading (e.g. "-40.0") with no "instant:" prefix
            nums = re.findall(r'-?\d+(?:\.\d+)?', s)
        if nums and all(abs(float(n) - (-40.0)) < 1e-9 for n in nums):
            ws.cell(row=r, column=rx_idx).fill = grey_fill
            for c in range(1, ws.max_column + 1):
                cell = ws.cell(row=r, column=c)
                f = cell.font
                cell.font = Font(
                    name=f.name, size=f.size, bold=f.bold, italic=f.italic,
                    vertAlign=f.vertAlign, underline=f.underline, strike=f.strike,
                    color=grey_color, family=f.family, scheme=f.scheme
                )

    # 6) "Maybe CT off" — devices that appear exactly 16 times: grey row + Note
    mark_ct_off(ws, key_col_name='Remote Device Name', threshold=16, note_text='maybe CT off')

    # 6b) cross-reference Interface Down: if the same link (Remote Device Name +
    #     Remote Device Port) also appears there, light-grey the row font and
    #     note "also interface down".
    ifd = find_sheet(wb, "Interface Down Errors", "Interface Down Errors (GPU)")
    if ifd:
        iws = wb[ifd]
        ih = [iws.cell(row=1, column=c).value for c in range(1, iws.max_column+1)]
        if 'Remote Device Name' in ih and 'Remote Device Port' in ih:
            ni = ih.index('Remote Device Name') + 1
            pi = ih.index('Remote Device Port') + 1
            ifd_links = set()
            for r in range(2, iws.max_row + 1):
                nm = iws.cell(row=r, column=ni).value
                if nm:
                    ifd_links.add((norm(nm), norm(iws.cell(row=r, column=pi).value)))

            header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
            if 'Note' not in header:
                add_note_column(ws)
                header = [ws.cell(row=1, column=c).value for c in range(1, ws.max_column+1)]
            rn_idx = header.index('Remote Device Name') + 1
            rp_idx = header.index('Remote Device Port') + 1
            note_idx = header.index('Note') + 1
